import { toast } from "react-toastify";

export const notificarSucesso = (str) => {
  toast.success(`${str}`, {
    position: "top-right",
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
  });
};

